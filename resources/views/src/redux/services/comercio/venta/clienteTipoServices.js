const onGrabar = async ( clientetipo ) => {
    return httpRequest( "post", webservices.wscomercioventaclientetipo_store, {
        codigo:  clientetipo.codigo,
        abreviatura:  clientetipo.abreviatura,
        descripcion:  clientetipo.descripcion,
    } ) . then ( ( result ) => {
        resultData( result );
        
        if ( result.response == 0 ) {
            C_Message( "warning", result.message );
            var errors = result.errors;
            for (let index in errors) {
                clientetipo.error[index] = true;
                clientetipo.message[index] = errors[index][0];
            }
            result.resultData = clientetipo;
        }

        if ( result.response == 1 ) {
            C_Message( "success", result.message );
        }
        console.log(result)
        return result;
    } );
};

