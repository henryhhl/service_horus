const onGrabar = async ( conceptocompra ) => {
    return httpRequest( "post", webservices.wscomerciocompraconceptocompra_store, {
        descripcion:  conceptocompra.descripcion,
    } ) . then ( ( result ) => {
        resultData( result );
        
        if ( result.response == 0 ) {
            C_Message( "warning", result.message );
            var errors = result.errors;
            for (let index in errors) {
                conceptocompra.error[index] = true;
                conceptocompra.message[index] = errors[index][0];
            }
            result.resultData = conceptocompra;
        }

        if ( result.response == 1 ) {
            C_Message( "success", result.message );
        }
        console.log(result)
        return result;
    } );
};

