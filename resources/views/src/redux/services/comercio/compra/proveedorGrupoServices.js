const onGrabar = async ( proveedorgrupo ) => {
    return httpRequest( "post", webservices.wscomerciocompraproveedorgrupo_store, {
        descripcion:  proveedorgrupo.descripcion,
    } ) . then ( ( result ) => {
        resultData( result );
        
        if ( result.response == 0 ) {
            C_Message( "warning", result.message );
            var errors = result.errors;
            for (let index in errors) {
                proveedorgrupo.error[index] = true;
                proveedorgrupo.message[index] = errors[index][0];
            }
            result.resultData = proveedorgrupo;
        }

        if ( result.response == 1 ) {
            C_Message( "success", result.message );
        }
        console.log(result)
        return result;
    } );
};

